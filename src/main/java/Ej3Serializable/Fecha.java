import java.io.Serializable;

public class Fecha implements Comparable<Fecha>, Serializable {

    private int dia;
    private int mes;
    private int anio;

    public Fecha(int dia, int mes, int anio) {
        this.dia = dia;
        this.mes = mes;
        this.anio = anio;
    }

    public int getAnio() {
        return anio;
    }

    public void setAnio(int anio) {
        this.anio = anio;
    }

    public int getDia() {
        return dia;
    }

    public void setDia(int dia) {
        this.dia = dia;
    }

    public int getMes() {
        return mes;
    }

    public void setMes(int mes) {
        this.mes = mes;
    }

    public int comparaTo(Fecha f) {
        int a = this.getAnio() - f.getAnio();
        int m = this.getMes() - f.getMes();
        int d = this.getDia() - f.getDia();

        if (a != 0) {
            return a;
        } else if (m != 0) {
            return m;
        } else {
            return d;
        }

    }

    @Override
    public String toString() {
        return this.getDia() + "/" + this.getMes() + "/" + this.getAnio();
    }

    @Override
    public int compareTo(Fecha o) {
        throw new UnsupportedOperationException("Not supported yet.");
    }

}
